#include <stdio.h>

int	ft_str_uppercase(char *str)
{
	int	c;

	c = 0;
	while (str[c] != '\0')
	{
		if (!(str[c] >= 'A' && str[c] <= 'Z'))
		{
			return (0);
		}
		c++;
	}
	return (1);
}
