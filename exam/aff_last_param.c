#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;

	i = 0;
	if (ac > 1)
	{
		ac--;
		while (av[ac])
		{
			write (1, &av[ac][1], 1);
			i++;
		}
		write (1, "\n", 1);
	}
	else
	{
		write (1, "\n", 1);
	}
}
